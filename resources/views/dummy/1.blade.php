<span class="text-lg font-bold">🕌☪FAQ Tilawah Race☪🕌</span>
<br><br>
Assalamualaikum Insan LPDP sekalian.
Saking semangatnya tilawah race, banyak yang tanya nih soal ketentuan Tilawah Race supaya lebih jelas mimin rangkumin FAQ Tilawah Race berikut;
<br><br>
<ul class="list-disc px-4">
<li><span class="font-bold">Q: Siapa aja yang boleh ikutan?</span>
<br>A : Seluruh Insan LPDP ; pegawai, driver, OB boleh mengikuti Tilawah race dan mendapatkan kesempatan yang sama mengejar hadiah utama
</li>

<li><span class="font-bold">Q: kapan pelaporan Tilawah Race dimulai?</span>
<br>A : Tilawah Race dimulai sejak tanggal 18 Maret 2024 (pkl. 18.30 WIB) sampai dengan 5 April 2024 (pkl. 18.30 WIB). Pelaporan Sebelum dan Setelah itu tidak dihitung.
</li>

<li><span class="font-bold">Q: Apakah bacaannya boleh mulai dari tengah-tengah?</span>
<br>A : Tidak boleh. Untuk memudahkan penghitungan score, pelaporan Tilawah Race dimulai dari Q.S. Al-fatihah.
</li>

<li><span class="font-bold">Q: kapan boleh tilawahnya?</span>
<br>A : Tilawah dilakukan diluar jam kerja sesuai dengan waktu clock in/clock out. Boleh juga tilawah di jam istirahat (12.00-12.30 WIB seuai SE) Laporan tilawah ketika jam kerja tidak akan dihitung.
</li>

<li><span class="font-bold">Q: Ooh, boleh tilawah di jam istirahat?</span>
<br>A : Boleh, jam 12.00-12.30 WIB sesuai SE
</li>
